<h1 tabindex="2" role="presentation">Convocatoria</h1>
<p tabindex="2" >Ingreso a Licenciatura 2015 </p>


<div tabindex="2" >
    <p class="justificado"> La Universidad Veracruzana a través de la Dirección General de Administración Escolar, dependiente de la Secretaría Académica, con base en el artículo 178 del Estatuto General y los artículos 13 y 15 del Estatuto de los Alumnos 2008
    </p>
</div>
    
<h2 tabindex="2" role="presentation">Convoca</h2>
<div tabindex="2" >

<p class="justificado"> 
 A las personas interesadas en participar en el proceso de admisión para ingresar al Ciclo Escolar agosto 2015- enero 2016 y cursar un programa educativo (carrera) de nivel Licenciatura en las modalidades escolarizada, abierta, a distancia, virtual y de nivel Técnico Superior Universitario; a presentar el Examen de Ingreso que se realizará los días 23 y 24 de Mayo de 2015, conforme a los siguientes:
</div>
<h2 tabindex="2"  role="presentation"> Requisitos </h2>


<p tabindex="2"> 1.	Realizar el registro por Internet con base al Instructivo de Registro del 23 de febrero al 7 de abril de 2015.    </p> 
<p tabindex="2"> 2. 	Realizar el pago de derechos del Examen indicados en la Orden de Pago del 23 de febrero al 8 de abril de 2015. </p> 
<p tabindex="2"> a.	Nacionales  $   665.00  (Seis Cientos Sesenta y Cinco pesos 00/100 M.N.) </p> 
<p tabindex="2"> b.	Extranjeros $1,994.00 (Un Mil Novecientos Noventa y Cuatro pesos 00/100 M.N.)</p> 
<p tabindex="2"> c.	Podrán condonar quienes tengan ese derecho. Ver trámite en: 
    www.uv.mx/dgrh/tramites-servicios/cond-insc/descripcion-ci/</p>
<p tabindex="2"> d.     Para el Programa educativo de Enseñanza del Inglés (modalidad virtual), además del EXANI II Admisión, deberán cubrir una certificación de lengua inglesa indicadas en el portal de la Facultad. Consulte el Portal de la Convocatoria en el botón: Programas Educativos (Carreras).</p>
<p tabindex="2"> 3.	Presentar el Examen de Ingreso en la fecha, hora y lugar indicados en la Credencial para Examen. Consulte el Portal de la Convocatoria en el botón: Guías de Examen.</p>
<p tabindex="2"> a.	Para todos los programas educativos se aplicará el examen EXANI II Admisión (Examen Nacional de Ingreso a Licenciatura).
<p tabindex="2"> b.	Para los programas educativos de Economía, Tecnologías Computacionales, Redes y Servicio de Cómputo e Ingeniería de Software además del EXANI II Admisión se aplicará el EXANI II Diagnóstico.  </p>
<p tabindex="2"> c.	Para los programa educativos de Educación Física Deporte y Recreación, Técnico Histotecnólogo y Embalsamador y del área académica de Artes de nivel licenciatura, además del EXANI II Admisión, deberán presentar un examen de aptitudes y habilidades artísticas respectivamente. Para tal fin, comunicarse a la facultad. Consulte el Portal de la Convocatoria en el botón: Guías de Examen.  </p>
<p tabindex="2"> d.	Para el programa educativo Gestión Intercultural para el Desarrollo de UVI, únicamente se aplicará un examen de competencias básicas y se realizará una entrevista personal. Otros requisitos en www.uv.mx/uvi </p>

<h2 tabindex="2"  role="presentation"> Condiciones </h2>

<p tabindex="2" > Del registro por Internet en www.uv.mx/escolar/licenciatura2015:  </p> 
<p tabindex="2"> 1.	Leer y aceptar términos y condiciones de la presente Convocatoria. </p> 
<p tabindex="2"> 2. 	Proporcionar los datos solicitados, seleccionar un programa educativo y completar registro.  </p> 
<p tabindex="2"> 3.    Realizar el pago de derechos del Examen.  </p> 
<p tabindex="2"> 4.    Subir al sistema una foto digital reciente del 25 de febrero al 11 de mayo de 2015. La foto deberá cumplir con las característica establecidas en el botón: Cómo hacer la foto.  </p> 
<p tabindex="2"> a.    Dos días hábiles después de subir la foto, deberá ingresar al portal con el Folio UV y contraseña para informarse si fue aceptada, en caso contrario subir una nueva foto.</p>
<p tabindex="2"> 5. 	Imprimir Credencial para Examen del 20 de abril al 20 de mayo de 2015.  </p>
<p tabindex="2"> 6.    Los aspirantes a un Programa educativo del área académica de Artes, que así lo decidan, podrán solicitar otro Programa educativo de la misma u otra área académica, es decir tener dos solicitudes. </p>
<p tabindex="2">De la aplicación del examen EXANI II:</h2>
<p tabindex="2"> 7.	Presentar el Examen de Ingreso el 23 o 24 de Mayo de 2015 en la sede y hora indicados en la Credencial para Examen.</p>
<p tabindex="2"> 8.	Mostrar la Credencial para Examen y una identificación en original con fotografía reciente. Por ejemplo: credencial de elector,  cartilla de servicio militar nacional, pasaporte, licencia de conducir, credencial escolar, constancia o certificado de estudios con fotografía. </p>
<p tabindex="2"> 9.	Llevar lápiz del número 2 ó 2 1/2, goma, sacapuntas y calculadora de funciones básicas (suma, resta, multiplicación y división). </p>
<p tabindex="2"> 10.	El Examen EXANI II Admisión tiene una duración de dos horas y treinta minutos. </p>
<p tabindex="2"> 11.	Los aspirantes a los Programas educativos de Economía, Tecnologías Computacionales, Redes y Servicio de Cómputo e Ingeniería de Software, presentarán el Examen 
en una sede de la región Xalapa y deberán considerar dos horas y 30 minutos para el EXANI II Admisión y dos horas para el EXANI II Diagnóstico. </h2>
<h2 tabindex="2" role="presentation"> De la aplicación de los exámenes de aptitudes y habilidades artísticas: </h2>
<p tabindex="2"> 12.	Consultar el Portal de la Convocatoria en el botón: Guías de Examen el día, hora y lugar para presentar el Examen de aptitudes, o bien en el portal de la 
Facultad a la que pertenece el Programa educativo seleccionado. </p>
<p tabindex="2"> 13.	Comprobar haber presentado el Examen EXANI II Admisión al momento de presentar el Examen de aptitudes, mostrando la Credencial para Examen etiquetada y firmada 
por el aplicador. </p>
<h2 tabindex="2" role="presentation">De los resultados:</h2>
<p tabindex="2"> 14.	Consultar los resultados por Internet en el portal de la Convocatoria a partir del  15 de junio de 2015.  </p>
<p tabindex="2"> 15.	Los aspirantes a cada Programa educativo se ordenarán en forma descendente en función del puntaje obtenido en el o los exámenes.</p>
<p tabindex="2"> 16.	En ese orden se asignará el derecho de inscripción hasta completar el número de lugares disponible en cada Programa educativo.   </p>
<p tabindex="2"> 17.	Para el Programa educativo de Danza, la asignación de lugares está determinado por el puntaje mínimo en el Examen de habilidades artísticas. Consulte el Portal 
de la Convocatoria en el botón: Guías de Examen.</p>
<p tabindex="2"> De la inscripción: </h2>
<p tabindex="2"> 18.	Las inscripciones se realizarán del 3 al 7 de agosto de 2015. </p>
<p tabindex="2"> 19.	La inscripción se realizará en la Facultad correspondiente, en la fecha y hora asignada en la Ficha de Inscripción.   </p>
<p tabindex="2"> 20.	Mostrar identificación oficial en original con fotografía y Ficha de Inscripción. </p>
<p tabindex="2"> 21.	Para inscribirse es obligatorio entregar los siguientes documentos:</p>
<p tabindex="2"> a.	Original de acta de nacimiento. </p>
<p tabindex="2"> b.	Original de certificado completo de estudios de bachillerato o constancia de estudios completos, (se aceptará temporalmente). </p>
<p tabindex="2"> c.	Comprobante de pago por los derechos de la inscripción.   </p>
<p tabindex="2"> 22.	Los aspirantes con derecho a inscripción deberán cumplir además con los requisitos establecidos por la Facultad. </p>
<p tabindex="2"> De los lugares disponibles: </p>
<p tabindex="2"> 23.	Se publicará en el Portal de la Convocatoria el 10 de agosto de 2015 el total de lugares disponibles. Consulte el Portal de la Convocatoria en el botón: 
Lugares Disponibles.</p
> 
<p tabindex="2"> 24.	Podrán solicitar un lugar disponible, los días 10 y 11 de agosto de 2015, los aspirantes que no obtuvieron un lugar con derecho a inscripción en Primera Lista 
y presentaron Examen de Ingreso en el mismo Programa educativo (carrera, región y modalidad).</p>  
<p tabindex="2"> 25.	Para participar por un lugar disponible, es obligatorio realizar una solicitud de registro en línea, siguiendo el procedimiento: “Cómo solicitar un lugar 
disponible”.  </p>
<p tabindex="2"> 26.	Los lugares con derecho a inscripción se asignarán en orden descendente, según el lugar ocupado en la Primera Lista de resultados del 15 de junio de 2015. En 
el caso de la Facultad de Música los lugares se asignarán únicamente en las opciones de instrumentos vacantes. </p>
<p tabindex="2"> 27.	Para el Programa educativo de Danza, no se ofrecerán lugares disponibles. </p>
<p tabindex="2"> 28.	Los resultados se publicarán el 12 de agosto de 2015. </p>
<p tabindex="2"> 29.	La inscripción se realizará el 13 de agosto de 2015. </p>
<p tabindex="2"> 30.	Para inscribirse es necesario cumplir con los puntos 19, 20, 21 y 22 de la presente Convocatoria. </p>

<h2 tabindex="2" role="presentation"> Consideraciones Importantes</h2>

<p tabindex="2"> 1.	Se cancelará el registro al aspirante que:</p>
<p tabindex="2"> a.	Sea suplantado o que proporcione documentos o información falsa.</p>
<p tabindex="2"> b.	Cuente con matrícula vigente o estatus de baja definitiva en la Universidad Veracruzana en el mismo Programa educativo del cual solicitó Examen de Ingreso en 
la presente Convocatoria.   </p>
<p tabindex="2"> 2.	No está permitido el ingreso a la sede del examen quien:</p>
<p tabindex="2"> a.	Esté en estado de ebriedad o con aliento alcohólico. </p>
<p tabindex="2"> b.	Porte gorra, sombrero o capucha, teléfono celular, audífonos o cualquier dispositivo electrónico (excepto calculadora simple), así como lentes para sol.   </p>
<p tabindex="2"> 3.	La Universidad Veracruzana se reserva el derecho de verificar, sin excepción alguna y en cualquier momento, la identidad de los aspirantes presentados y la 
validez de los documentos exhibidos. </p>
<p tabindex="2"> 4.	Efectuado el pago de derechos no se hará ninguna devolución.  </p>
<p tabindex="2"> 5.	Los aspirantes al programa educativo Enseñanza del Inglés (modalidad virtual) deberán cubrir una certificación de lengua inglesa indicadas en el portal de la facultad. Consulte el Portal de la Convocatoria en el botón: Programas Educativos. </p>
<p tabindex="2"> 6.	Los aspirantes mexicanos o extranjeros que estudiaron el bachillerato en otro país deberán presentar Oficio de resolución de revalidación de estudios completos 
de bachillerato.</p>
<p tabindex="2"> 7.	Los aspirantes extranjeros deberán presentar documentación que acredite su estancia legal en el país.</p>
<p tabindex="2"> 8.	Es responsabilidad del interesado realizar correctamente el registro y concluirlo según las fechas establecidas en la presente Convocatoria.</p>
<p tabindex="2"> 9.	La Universidad Veracruzana no ofrece ningún curso de preparación para el Examen de Ingreso. </p>
<p tabindex="2"> 10.	Los asuntos no previstos en la presente Convocatoria serán resueltos por la Comisión Técnico Académica de Ingreso y Escolaridad. </p>
